      SUBROUTINE GET_AMP(P,NHEL,IC,FLAV_IDX,AMP)
      USE MODEL_OBJECT
C     
C     Process: e+ e- > a a a
C     
CF2PY INTENT(OUT) :: AMP
CF2PY INTENT(IN) :: NHEL
CF2PY INTENT(IN) :: P(0:3,NEXTERNAL)
CF2PY INTENT(IN) :: IC
CF2PY INTENT(IN) :: FLAV_IDX

      USE ALOHA_OBJECT
      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NGRAPHS
      PARAMETER (NGRAPHS=6)
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=5)
      INTEGER    NWAVEFUNCS, NCOLOR
      PARAMETER (NWAVEFUNCS=9, NCOLOR=1)
      REAL*8     ZERO
      PARAMETER (ZERO=0D0)
C     
C     ARGUMENTS
C     
      REAL*8 P(0:3,NEXTERNAL)
      INTEGER NHEL(NEXTERNAL), IC(NEXTERNAL)
      INTEGER FLAV_IDX
      INTEGER FLAVOR(NEXTERNAL)
C     
C     LOCAL VARIABLES
C     
      COMPLEX*16 AMP(NGRAPHS)
      TYPE(ALOHA) W(NWAVEFUNCS)
      COMPLEX*16 DUM0,DUM1
      DATA DUM0, DUM1/(0D0, 0D0), (1D0, 0D0)/
      DOUBLE PRECISION BWCUTOFF
C     Flavor table for the FLAV_IDX -> FLAVOR rebuild.
      INTEGER NMASK_FLAV
      PARAMETER (NMASK_FLAV=1)
      INTEGER MASK_J
      INTEGER FLAV_TABLE(NEXTERNAL, NMASK_FLAV)
      DATA FLAV_TABLE / 1, 1, 1, 1, 1 /
C     
C     GLOBAL VARIABLES
C     
      INCLUDE 'coupl.inc'

C     
C     
      BWCUTOFF=15  ! use if $ syntax is defined in the process
C     Rebuild FLAVOR(NEXTERNAL) from the resolved flavor index.
      IF (FLAV_IDX .GE. 1 .AND. FLAV_IDX .LE. NMASK_FLAV) THEN
        DO MASK_J = 1, NEXTERNAL
          FLAVOR(MASK_J) = FLAV_TABLE(MASK_J, FLAV_IDX)
        ENDDO
      ELSE
        DO MASK_J = 1, NEXTERNAL
          FLAVOR(MASK_J) = FLAV_TABLE(MASK_J, 1)
        ENDDO
      ENDIF
      CALL OXXXXX(P(0,1),ZERO,NHEL(1),-1, FLAVOR(1),W(1))
      CALL IXXXXX(P(0,2),ZERO,NHEL(2),+1, FLAVOR(2),W(2))
      CALL VXXXXX(P(0,3),ZERO,NHEL(3),+1,W(3))
      CALL VXXXXX(P(0,4),ZERO,NHEL(4),+1,W(4))
      CALL VXXXXX(P(0,5),ZERO,NHEL(5),+1,W(5))
      CALL FVOXXX(W(1),W(3),MGVX12,ZERO,ZERO,W(6))
      CALL FVIXXX(W(2),W(4),MGVX12,ZERO,ZERO,W(7))
C     Amplitude(s) for diagram number 1
      CALL IOVXXX(W(7),W(6),W(5),MGVX12,AMP(1))
      CALL FVIXXX(W(2),W(5),MGVX12,ZERO,ZERO,W(8))
C     Amplitude(s) for diagram number 2
      CALL IOVXXX(W(8),W(6),W(4),MGVX12,AMP(2))
      CALL FVOXXX(W(1),W(4),MGVX12,ZERO,ZERO,W(6))
      CALL FVIXXX(W(2),W(3),MGVX12,ZERO,ZERO,W(9))
C     Amplitude(s) for diagram number 3
      CALL IOVXXX(W(9),W(6),W(5),MGVX12,AMP(3))
C     Amplitude(s) for diagram number 4
      CALL IOVXXX(W(8),W(6),W(3),MGVX12,AMP(4))
      CALL FVOXXX(W(1),W(5),MGVX12,ZERO,ZERO,W(6))
C     Amplitude(s) for diagram number 5
      CALL IOVXXX(W(9),W(6),W(4),MGVX12,AMP(5))
C     Amplitude(s) for diagram number 6
      CALL IOVXXX(W(7),W(6),W(3),MGVX12,AMP(6))

      END

      SUBROUTINE GET_JAMP(AMP,JAMP)
C     
C     Process: e+ e- > a a a
C     
CF2PY INTENT(OUT) :: JAMP
CF2PY INTENT(IN) :: AMP

      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NGRAPHS
      PARAMETER (NGRAPHS=6)
      INTEGER    NCOLOR
      PARAMETER ( NCOLOR=1)
      COMPLEX*16 IMAG1
      PARAMETER (IMAG1=(0D0,1D0))
      COMPLEX*16 AMP(NGRAPHS), JAMP(NCOLOR), TMP_JAMP(0)

      JAMP(1) = (-1.000000000000000D+00)*AMP(1)+(-1.000000000000000D
     $ +00)*AMP(2)+(-1.000000000000000D+00)*AMP(3)+(
     $ -1.000000000000000D+00)*AMP(4)+(-1.000000000000000D+00)*AMP(5)
     $ +(-1.000000000000000D+00)*AMP(6)
      END

      SUBROUTINE GET_MATRIX(JAMP,MATRIX)
C     
C     Process: e+ e- > a a a
C     
      IMPLICIT NONE
C     
C     CONSTANTS
C     
CF2PY INTENT(OUT) :: MATRIX
CF2PY INTENT(IN) :: JAMP


      INTEGER    NCOLOR
      PARAMETER (NCOLOR=1)
      REAL*8     ZERO,MATRIX
      PARAMETER (ZERO=0D0)
C     

C     LOCAL VARIABLES
C     
      INTEGER I,J
      COMPLEX*16 ZTEMP

      INTEGER CF_INDEX
      INTEGER CF(NCOLOR*(NCOLOR+1)/2)
      INTEGER DENOM
      COMMON /COLOR_MATRIX/ CF,DENOM
      COMPLEX*16 JAMP(NCOLOR), TMP_JAMP(0)
      COMPLEX*16 DUM0,DUM1
      DATA DUM0, DUM1/(0D0, 0D0), (1D0, 0D0)/
C     

C     COLOR DATA
C     

      MATRIX = 0.D0
      CF_INDEX = 0
      DO I = 1, NCOLOR
        ZTEMP = (0.D0,0.D0)
        DO J = I, NCOLOR
          CF_INDEX = CF_INDEX + 1
          ZTEMP = ZTEMP + CF(CF_INDEX)*JAMP(J)
        ENDDO
        MATRIX = MATRIX+ZTEMP*DCONJG(JAMP(I))/DENOM
      ENDDO
      END



      SUBROUTINE GET_INTER(JAMP_1,JAMP_2, INTER)

CF2PY INTENT(OUT) :: INTER
CF2PY INTENT(IN) :: JAMP_1
CF2PY INTENT(IN) :: JAMP_2

      INTEGER I,J
      INTEGER NCOLOR
      PARAMETER (NCOLOR=1)
      INTEGER CF_INDEX
      INTEGER CF(NCOLOR*(NCOLOR+1)/2)
      INTEGER DENOM
      COMMON /COLOR_MATRIX/ CF,DENOM
      COMPLEX*16 JAMP_1(NCOLOR),JAMP_2(NCOLOR),INTER

C     COLOR DATA
C     

      INTER = (0.D0,0.D0)
      CF_INDEX = 0
      DO I = 1, NCOLOR
C       ZTEMP = DCONJG(JAMP_2(I))
        DO J=I, NCOLOR
          CF_INDEX = CF_INDEX +1
          INTER = INTER + CF(CF_INDEX) * (JAMP_1(J) * DCONJG(JAMP_2(I))
     $      +JAMP_1(I) * DCONJG(JAMP_2(J)))
        ENDDO
      ENDDO
      INTER = INTER/ (2D0*DENOM)

      END



      SUBROUTINE  GET_DENSITY(P, POS, N_CHANGING, ALLOW_HEL, N_COMB,
     $  FLAVOR, ALPHAS, INTER)
C     P momenta
C     NHEL base of helicity that are not changing
C     POS(N_CHNGING): position of the changing helicity
C     n_changing: number of changing helicity
C     ALLOW_HEL(NCOMB, N_CHANGING): combination of helicity to
C      consider (all jamp computed)
C     INTER((NCOMB*NCOMB+1)/2: all interference term (not the
C      symmetric one)
      USE MODEL_OBJECT
      IMPLICIT NONE
CF2PY INTENT(IN) :: P(0:3,5)
CF2PY INTENT(IN) :: POS(N_CHANGING)
CF2PY INTENT(IN) :: N_CHANGING
CF2PY INTENT(IN) :: ALLOW_HEL(N_CHANGING*N_COMB)
CF2PY INTENT(IN) :: N_COMB
CF2PY INTENT(IN) :: FLAVOR(5)
CF2PY INTENT(IN) :: ALPHAS
CF2PY INTENT(OUT) :: INTER(N_COMB*(N_COMB+1)/2)
C     
C     
C     ARGUMENTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=5)
      REAL*8 P(0:3,NEXTERNAL)
      INTEGER THISNHEL(NEXTERNAL)
      INTEGER N_CHANGING, N_COMB
      INTEGER POS(*)
      INTEGER ALLOW_HEL(*)
      INTEGER FLAVOR(NEXTERNAL)
      DOUBLE PRECISION ALPHAS
      DOUBLE COMPLEX INTER(*)
      INTEGER NINTER
      INTEGER NB_NHEL
      DOUBLE COMPLEX, ALLOCATABLE :: TMP_INTER(:)
      PARAMETER (NB_NHEL=32)
C     LOCAL
      INTEGER I,IHEL,IPART
      DOUBLE PRECISION PI
C     
      INTEGER NHEL(NEXTERNAL,NB_NHEL),NTRY
C     put in common block to expose this variable to python interface
      COMMON/PROCESS_NHEL/NHEL
C     
C     include coupling definition to update the value of alphas
C     
      INCLUDE 'coupl.inc'

      NINTER = N_COMB*(N_COMB+1)/2
      ALLOCATE(TMP_INTER(NINTER))
      TMP_INTER(:) = (0D0, 0D0)

      DO I=1, N_COMB*(N_COMB+1)/2
        INTER(I) = 0
      ENDDO

      IF (ALPHAS.NE.0D0) THEN
        PI = 3.141592653589793D0
        G = 2* DSQRT(ALPHAS*PI)
        CALL UPDATE_AS_PARAM()
      ENDIF
      DO IHEL =1, NB_NHEL
        THISNHEL(:) = NHEL(:, IHEL)
        DO IPART=1,N_CHANGING
          IF(THISNHEL(POS(IPART)).NE.ALLOW_HEL(IPART)) GOTO 10  !BYPASS COMPUTATION FOR HELICITY
        ENDDO
        TMP_INTER(:) = 0
        CALL  GET_ALL_INTER(P, THISNHEL, POS, N_CHANGING, ALLOW_HEL,
     $    N_COMB, FLAVOR, TMP_INTER)
        DO I = 1, N_COMB*(N_COMB+1)/2
          INTER(I) = INTER(I) + TMP_INTER(I)
        ENDDO
 10   ENDDO
      RETURN
      DEALLOCATE(TMP_INTER)
      END

      SUBROUTINE  GET_ALL_INTER(P, NHEL, POS, N_CHANGING, ALLOW_HEL,
     $  N_COMB, FLAVOR, INTER)
C     P momenta
C     NHEL base of helicity that are not changing
C     POS(N_CHNGING): position of the changing helicity
C     n_changing: number of changing helicity
C     ALLOW_HEL(NCOMB, N_CHANGING): combination of helicity to
C      consider (all jamp computed)
C     INTER((NCOMB*NCOMB+1)/2: all interference term (not the
C      symmetric one)
      IMPLICIT NONE
CF2PY INTENT(IN) :: P(0:3,5)
CF2PY INTENT(IN) :: NHEL(5)
CF2PY INTENT(IN) :: POS(N_CHANGING)
CF2PY INTENT(IN) :: N_CHANGING
CF2PY INTENT(IN) :: ALLOW_HEL(N_CHANGING*N_COMB)
CF2PY INTENT(IN) :: N_COMB
CF2PY INTENT(IN) :: FLAVOR(5)
CF2PY INTENT(OUT) :: INTER(NCOMB*(NCOMB+1)/2)
C     
C     
C     ARGUMENTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=5)
      REAL*8 P(0:3,NEXTERNAL)
      INTEGER NHEL(NEXTERNAL)
      INTEGER N_CHANGING, N_COMB
      INTEGER POS(*)
      INTEGER ALLOW_HEL(*)
      INTEGER FLAVOR(NEXTERNAL)
      DOUBLE COMPLEX INTER(*)
C     
C     Intermediate array
C     
      INTEGER    NGRAPHS
      PARAMETER (NGRAPHS=6)
      INTEGER    NCOLOR
      PARAMETER (NCOLOR=1)
      INTEGER IC(NEXTERNAL)

      DOUBLE COMPLEX AMP(NGRAPHS)
      DOUBLE COMPLEX, ALLOCATABLE, SAVE :: JAMP(:,:)
      INTEGER, SAVE :: S_NCOMB = 0

C     
C     LOCAL
C     
      INTEGER I,J,SOL,N
      INTEGER FLAV_IDX
      INTEGER GET_FLAVOR_INDEX

      IF (ALLOCATED(JAMP) .AND. S_NCOMB.NE.N_COMB) THEN
        DEALLOCATE(JAMP)
      ENDIF

      IF (.NOT.ALLOCATED(JAMP)) THEN
        S_NCOMB=N_COMB
        ALLOCATE(JAMP(NCOLOR, N_COMB))
      ENDIF
C     ----------
C     BEGIN CODE
C     ----------
      IC(:)=1
      FLAV_IDX = GET_FLAVOR_INDEX(FLAVOR)
      DO I = 1, N_COMB
        DO N = 1, N_CHANGING
          NHEL(POS(N)) = ALLOW_HEL((I-1)*N_CHANGING+N)
        ENDDO
        CALL GET_AMP(P,NHEL,IC,FLAV_IDX,AMP)
        CALL GET_JAMP(AMP,JAMP(1,I))
      ENDDO

      SOL = 0
      DO I = 1, N_COMB
        DO J= I, N_COMB
          SOL = SOL +1
          CALL GET_INTER(JAMP(1,I), JAMP(1,J), INTER(SOL))
        ENDDO
      ENDDO


      RETURN
      END




