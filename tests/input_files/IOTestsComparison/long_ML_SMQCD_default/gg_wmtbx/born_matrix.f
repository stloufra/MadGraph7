      LOGICAL FUNCTION ML5_0_IS_BORN_HEL_SELECTED(HELID)
      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=5)
      INTEGER    NCOMB
      PARAMETER (NCOMB=48)
C     
C     ARGUMENTS
C     
      INTEGER HELID
C     
C     LOCALS
C     
      INTEGER I,J
      LOGICAL FOUNDIT
C     
C     GLOBALS
C     
      INTEGER HELC(NEXTERNAL,NCOMB)
      COMMON/ML5_0_PROCESS_NHEL/HELC

      INTEGER POLARIZATIONS(0:NEXTERNAL,0:5)
      COMMON/ML5_0_BORN_BEAM_POL/POLARIZATIONS
C     ----------
C     BEGIN CODE
C     ----------

      ML5_0_IS_BORN_HEL_SELECTED = .TRUE.
      IF (POLARIZATIONS(0,0).EQ.-1) THEN
        RETURN
      ENDIF

      DO I=1,NEXTERNAL
        IF (POLARIZATIONS(I,0).EQ.-1) THEN
          CYCLE
        ENDIF
        FOUNDIT = .FALSE.
        DO J=1,POLARIZATIONS(I,0)
          IF (HELC(I,HELID).EQ.POLARIZATIONS(I,J)) THEN
            FOUNDIT = .TRUE.
            EXIT
          ENDIF
        ENDDO
        IF(.NOT.FOUNDIT) THEN
          ML5_0_IS_BORN_HEL_SELECTED = .FALSE.
          RETURN
        ENDIF
      ENDDO

      RETURN
      END


      INTEGER FUNCTION ML5_0_BROKEN_SYM(FLAV)

      INCLUDE 'nexternal.inc'
      INTEGER FLAV(NEXTERNAL)
      INTEGER PID(NINCOMING+1:NEXTERNAL)
      INTEGER I,J
      INTEGER N_BROK, N_TOT
      INTEGER OLD_FACTOR

      OLD_FACTOR = 1

      PID = [-24, 6, -5]
C     get new symmetry factor      
      DO I=NINCOMING+1, NEXTERNAL
        IF (PID(I).EQ.0) CYCLE
        N_TOT=1
        DO J = I+1,NEXTERNAL
          IF (PID(I).EQ.PID(J).AND.FLAV(I).EQ.FLAV(J))THEN
            PID(J) = 0
            N_TOT = N_TOT + 1
            OLD_FACTOR = OLD_FACTOR/N_TOT
          ENDIF
        ENDDO
      ENDDO
      ML5_0_BROKEN_SYM = OLD_FACTOR
      RETURN
      END


