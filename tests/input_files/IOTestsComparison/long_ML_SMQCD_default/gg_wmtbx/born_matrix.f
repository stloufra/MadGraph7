      SUBROUTINE ML5_0_SMATRIX(P, FLAVOR, ANS)
C     
C     Generated by MadGraph5_aMC@NLO v. %(version)s, %(date)s
C     By the MadGraph5_aMC@NLO Development Team
C     Visit launchpad.net/madgraph5 and amcatnlo.web.cern.ch
C     
C     MadGraph5_aMC@NLO StandAlone Version
C     
C     Returns amplitude squared summed/avg over colors
C     and helicities
C     for the point in phase space P(0:3,NEXTERNAL)
C     
C     Process: g g > w- t b~ QCD<=2 QED<=1 [ virt = QCD ]
C     
      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=5)
      INTEGER    NINITIAL
      PARAMETER (NINITIAL=2)
      INTEGER NPOLENTRIES
      PARAMETER (NPOLENTRIES=(NEXTERNAL+1)*6)
      INTEGER                 NCOMB
      PARAMETER (             NCOMB=48)
      INTEGER HELAVGFACTOR
      PARAMETER (HELAVGFACTOR=4)
C     
C     ARGUMENTS 
C     
      REAL*8 P(0:3,NEXTERNAL),ANS
CF2PY INTENT(OUT) :: ANS
CF2PY INTENT(IN) :: P(0:3,NEXTERNAL)
CF2PY INTENT(IN) :: FLAVOR(NEXTERNAL)
C     
C     LOCAL VARIABLES 
C     
      INTEGER NHEL(NEXTERNAL,NCOMB),NTRY
C     put in common block to expose this variable to python interface
      COMMON/ML5_0_PROCESS_NHEL/NHEL
      REAL*8 T
      REAL*8 ML5_0_MATRIX
      INTEGER IHEL,IDEN, I, J
C     For a 1>N process, them BEAMTWO_HELAVGFACTOR would be set to 1.
      INTEGER BEAMS_HELAVGFACTOR(2)
      DATA (BEAMS_HELAVGFACTOR(I),I=1,2)/2,2/
      INTEGER FLAVOR(NEXTERNAL)
      LOGICAL GOODHEL(NCOMB)
      DATA NTRY/0/
      DATA GOODHEL/NCOMB*.FALSE./

C     
C     GLOBAL VARIABLES
C     
      INTEGER USERHEL
      COMMON/ML5_0_HELUSERCHOICE/USERHEL
      DATA USERHEL/-1/
      LOGICAL HELRESET
      COMMON/ML5_0_HELRESET/HELRESET
      DATA HELRESET/.TRUE./

      DATA (NHEL(I,   1),I=1,5) /-1,-1, 1,-1, 1/
      DATA (NHEL(I,   2),I=1,5) /-1,-1, 1,-1,-1/
      DATA (NHEL(I,   3),I=1,5) /-1,-1, 1, 1, 1/
      DATA (NHEL(I,   4),I=1,5) /-1,-1, 1, 1,-1/
      DATA (NHEL(I,   5),I=1,5) /-1,-1, 0,-1, 1/
      DATA (NHEL(I,   6),I=1,5) /-1,-1, 0,-1,-1/
      DATA (NHEL(I,   7),I=1,5) /-1,-1, 0, 1, 1/
      DATA (NHEL(I,   8),I=1,5) /-1,-1, 0, 1,-1/
      DATA (NHEL(I,   9),I=1,5) /-1,-1,-1,-1, 1/
      DATA (NHEL(I,  10),I=1,5) /-1,-1,-1,-1,-1/
      DATA (NHEL(I,  11),I=1,5) /-1,-1,-1, 1, 1/
      DATA (NHEL(I,  12),I=1,5) /-1,-1,-1, 1,-1/
      DATA (NHEL(I,  13),I=1,5) /-1, 1, 1,-1, 1/
      DATA (NHEL(I,  14),I=1,5) /-1, 1, 1,-1,-1/
      DATA (NHEL(I,  15),I=1,5) /-1, 1, 1, 1, 1/
      DATA (NHEL(I,  16),I=1,5) /-1, 1, 1, 1,-1/
      DATA (NHEL(I,  17),I=1,5) /-1, 1, 0,-1, 1/
      DATA (NHEL(I,  18),I=1,5) /-1, 1, 0,-1,-1/
      DATA (NHEL(I,  19),I=1,5) /-1, 1, 0, 1, 1/
      DATA (NHEL(I,  20),I=1,5) /-1, 1, 0, 1,-1/
      DATA (NHEL(I,  21),I=1,5) /-1, 1,-1,-1, 1/
      DATA (NHEL(I,  22),I=1,5) /-1, 1,-1,-1,-1/
      DATA (NHEL(I,  23),I=1,5) /-1, 1,-1, 1, 1/
      DATA (NHEL(I,  24),I=1,5) /-1, 1,-1, 1,-1/
      DATA (NHEL(I,  25),I=1,5) / 1,-1, 1,-1, 1/
      DATA (NHEL(I,  26),I=1,5) / 1,-1, 1,-1,-1/
      DATA (NHEL(I,  27),I=1,5) / 1,-1, 1, 1, 1/
      DATA (NHEL(I,  28),I=1,5) / 1,-1, 1, 1,-1/
      DATA (NHEL(I,  29),I=1,5) / 1,-1, 0,-1, 1/
      DATA (NHEL(I,  30),I=1,5) / 1,-1, 0,-1,-1/
      DATA (NHEL(I,  31),I=1,5) / 1,-1, 0, 1, 1/
      DATA (NHEL(I,  32),I=1,5) / 1,-1, 0, 1,-1/
      DATA (NHEL(I,  33),I=1,5) / 1,-1,-1,-1, 1/
      DATA (NHEL(I,  34),I=1,5) / 1,-1,-1,-1,-1/
      DATA (NHEL(I,  35),I=1,5) / 1,-1,-1, 1, 1/
      DATA (NHEL(I,  36),I=1,5) / 1,-1,-1, 1,-1/
      DATA (NHEL(I,  37),I=1,5) / 1, 1, 1,-1, 1/
      DATA (NHEL(I,  38),I=1,5) / 1, 1, 1,-1,-1/
      DATA (NHEL(I,  39),I=1,5) / 1, 1, 1, 1, 1/
      DATA (NHEL(I,  40),I=1,5) / 1, 1, 1, 1,-1/
      DATA (NHEL(I,  41),I=1,5) / 1, 1, 0,-1, 1/
      DATA (NHEL(I,  42),I=1,5) / 1, 1, 0,-1,-1/
      DATA (NHEL(I,  43),I=1,5) / 1, 1, 0, 1, 1/
      DATA (NHEL(I,  44),I=1,5) / 1, 1, 0, 1,-1/
      DATA (NHEL(I,  45),I=1,5) / 1, 1,-1,-1, 1/
      DATA (NHEL(I,  46),I=1,5) / 1, 1,-1,-1,-1/
      DATA (NHEL(I,  47),I=1,5) / 1, 1,-1, 1, 1/
      DATA (NHEL(I,  48),I=1,5) / 1, 1,-1, 1,-1/
      DATA IDEN/256/

      INTEGER POLARIZATIONS(0:NEXTERNAL,0:5)
      COMMON/ML5_0_BORN_BEAM_POL/POLARIZATIONS
      DATA ((POLARIZATIONS(I,J),I=0,NEXTERNAL),J=0,5)/NPOLENTRIES*-1/

C     
C     FUNCTIONS
C     
      LOGICAL ML5_0_IS_BORN_HEL_SELECTED
      INTEGER ML5_0_BROKEN_SYM
C     ----------
C     Check if helreset mode is on
C     ---------
      IF (HELRESET) THEN
        NTRY = 0
        DO I=1,NCOMB
          GOODHEL(I) = .FALSE.
        ENDDO
        HELRESET = .FALSE.
      ENDIF

C     ----------
C     BEGIN CODE
C     ----------
      IF(USERHEL.EQ.-1) NTRY=NTRY+1
C     When spin-2 particles are involved, the Helicity filtering is
C      dangerous for the 2->1 topology.
C     This is because depending on the MC setup the initial PS points
C      have back-to-back initial states
C     for which some of the spin-2 helicity configurations are zero.
C      But they are no longer zero
C     if the point is boosted on the z-axis. Remember that HELAS
C      helicity amplitudes are no longer
C     lorentz invariant with expternal spin-2 particles (only the
C      helicity sum is).
C     For this reason, we simply remove the filterin when there is
C      only three external particles.
      IF (NEXTERNAL.LE.3) THEN
        DO IHEL=1,NCOMB
          GOODHEL(IHEL)=.TRUE.
        ENDDO
      ENDIF
      ANS = 0D0
      DO IHEL=1,NCOMB
        IF (USERHEL.EQ.-1.OR.USERHEL.EQ.IHEL) THEN
          IF (GOODHEL(IHEL) .OR. NTRY .LT. 20.OR.USERHEL.NE.-1) THEN
            IF(NTRY.GE.2.AND.POLARIZATIONS(0,0).NE.
     $       -1.AND.(.NOT.ML5_0_IS_BORN_HEL_SELECTED(IHEL))) THEN
              CYCLE
            ENDIF
            T=ML5_0_MATRIX(P ,NHEL(1,IHEL),FLAVOR(1))
            IF(POLARIZATIONS(0,0).EQ.
     $       -1.OR.ML5_0_IS_BORN_HEL_SELECTED(IHEL)) THEN
              ANS=ANS+T
            ENDIF
            IF (T .NE. 0D0 .AND. .NOT.    GOODHEL(IHEL)) THEN
              GOODHEL(IHEL)=.TRUE.
            ENDIF
          ENDIF
        ENDIF
      ENDDO
      ANS=ANS/DBLE(IDEN)*ML5_0_BROKEN_SYM(FLAVOR)
      IF(USERHEL.NE.-1) THEN
        ANS=ANS*HELAVGFACTOR
      ELSE
        DO J=1,NINITIAL
          IF (POLARIZATIONS(J,0).NE.-1) THEN
            ANS=ANS*BEAMS_HELAVGFACTOR(J)
            ANS=ANS/POLARIZATIONS(J,0)
          ENDIF
        ENDDO
      ENDIF
      END


      REAL*8 FUNCTION ML5_0_MATRIX(P,NHEL,FLAVOR)
      USE MODEL_OBJECT
C     
C     Generated by MadGraph5_aMC@NLO v. %(version)s, %(date)s
C     By the MadGraph5_aMC@NLO Development Team
C     Visit launchpad.net/madgraph5 and amcatnlo.web.cern.ch
C     
C     Returns amplitude squared -- no average over initial
C      state/symmetry factor
C     for the point with external lines W(0:6,NEXTERNAL)
C     
C     Process: g g > w- t b~ QCD<=2 QED<=1 [ virt = QCD ]
C     
      USE ALOHA_OBJECT
      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NGRAPHS
      PARAMETER (NGRAPHS=8)
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=5)
      INTEGER    NWAVEFUNCS, NCOLOR
      PARAMETER (NWAVEFUNCS=9, NCOLOR=2)
      REAL*8     ZERO
      PARAMETER (ZERO=0D0)
      COMPLEX*16 IMAG1
      PARAMETER (IMAG1=(0D0,1D0))
C     
C     ARGUMENTS 
C     
      REAL*8 P(0:3,NEXTERNAL)
      INTEGER NHEL(NEXTERNAL)
      INTEGER FLAVOR(NEXTERNAL)
C     
C     LOCAL VARIABLES 
C     
      INTEGER I,J
      COMPLEX*16 ZTEMP
      INTEGER CF_INDEX
      INTEGER CF(NCOLOR*(NCOLOR+1)/2)
      INTEGER DENOM
      COMPLEX*16 AMP(NGRAPHS), JAMP(NCOLOR), TMP_JAMP(1)
      TYPE(ALOHA) W(NWAVEFUNCS)
      COMPLEX*16 DUM0,DUM1
      DATA DUM0, DUM1/(0D0, 0D0), (1D0, 0D0)/
C     
C     GLOBAL VARIABLES
C     
      INCLUDE 'coupl.inc'

C     
C     COLOR DATA
C     
      DATA DENOM/3/
      DATA (CF(I),I=  1,  2) /16,-4/
C     1 T(1,2,4,5)
      DATA (CF(I),I=  3,  3) /16/
C     1 T(2,1,4,5)
C     ----------
C     BEGIN CODE
C     ----------
      CALL VXXXXX(P(0,1),ZERO,NHEL(1),-1,W(1))
      CALL VXXXXX(P(0,2),ZERO,NHEL(2),-1,W(2))
      CALL VXXXXX(P(0,3),MDL_MW,NHEL(3),+1,W(3))
      CALL OXXXXX(P(0,4),MDL_MT,NHEL(4),+1, FLAVOR(4),W(4))
      CALL IXXXXX(P(0,5),MDL_MB,NHEL(5),-1, FLAVOR(5),W(5))
      CALL VVV1P0_1(W(1),W(2),GC_4,ZERO,ZERO,W(6))
      CALL FFV2_1(W(4),W(3),GC_11,MDL_MB,ZERO,W(7))
C     Amplitude(s) for diagram number 1
      CALL FFV1_0(W(5),W(7),W(6),GC_5,AMP(1))
      CALL FFV2_2(W(5),W(3),GC_11,MDL_MT,MDL_WT,W(8))
C     Amplitude(s) for diagram number 2
      CALL FFV1_0(W(8),W(4),W(6),GC_5,AMP(2))
      CALL FFV1_1(W(4),W(1),GC_5,MDL_MT,MDL_WT,W(6))
      CALL FFV1_2(W(5),W(2),GC_5,MDL_MB,ZERO,W(9))
C     Amplitude(s) for diagram number 3
      CALL FFV2_0(W(9),W(6),W(3),GC_11,AMP(3))
C     Amplitude(s) for diagram number 4
      CALL FFV1_0(W(8),W(6),W(2),GC_5,AMP(4))
      CALL FFV1_2(W(5),W(1),GC_5,MDL_MB,ZERO,W(6))
      CALL FFV1_1(W(4),W(2),GC_5,MDL_MT,MDL_WT,W(5))
C     Amplitude(s) for diagram number 5
      CALL FFV2_0(W(6),W(5),W(3),GC_11,AMP(5))
C     Amplitude(s) for diagram number 6
      CALL FFV1_0(W(6),W(7),W(2),GC_5,AMP(6))
C     Amplitude(s) for diagram number 7
      CALL FFV1_0(W(8),W(5),W(1),GC_5,AMP(7))
C     Amplitude(s) for diagram number 8
      CALL FFV1_0(W(9),W(7),W(1),GC_5,AMP(8))
      TMP_JAMP(1) = AMP(1) +  AMP(2)  ! used 2 times
      JAMP(1) = (-1.000000000000000D+00)*AMP(3)+(-1.000000000000000D
     $ +00)*AMP(4)+(-1.000000000000000D+00)*AMP(8)
     $ +((0.000000000000000D+00,1.000000000000000D+00))*TMP_JAMP(1)
      JAMP(2) = (-1.000000000000000D+00)*AMP(5)+(-1.000000000000000D
     $ +00)*AMP(6)+(-1.000000000000000D+00)*AMP(7)
     $ +((0.000000000000000D+00,-1.000000000000000D+00))*TMP_JAMP(1)

      ML5_0_MATRIX = 0.D0
      CF_INDEX = 0
      DO I = 1, NCOLOR
        ZTEMP = (0D0,0D0)
        DO J = I, NCOLOR
          CF_INDEX = CF_INDEX +1
          ZTEMP = ZTEMP + CF(CF_INDEX)*JAMP(J)
        ENDDO
        ML5_0_MATRIX = ML5_0_MATRIX+REAL(ZTEMP*DCONJG(JAMP(I)))
      ENDDO
      ML5_0_MATRIX = ML5_0_MATRIX / DENOM

      END

