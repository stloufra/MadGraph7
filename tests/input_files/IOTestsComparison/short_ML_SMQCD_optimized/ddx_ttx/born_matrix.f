      SUBROUTINE ML5_0_GET_MATRIX(JAMP,MATRIX)
C     
C     Process: d d~ > t t~ QCD<=2 QED=0 [ virt = QCD ]
C     
      IMPLICIT NONE
C     
C     CONSTANTS
C     
CF2PY INTENT(OUT) :: MATRIX
CF2PY INTENT(IN) :: JAMP


      INTEGER    NCOLOR
      PARAMETER (NCOLOR=2)
      REAL*8     ZERO,MATRIX
      PARAMETER (ZERO=0D0)
C     

C     LOCAL VARIABLES
C     
      INTEGER I,J
      COMPLEX*16 ZTEMP

      INTEGER CF_INDEX
      INTEGER ML5_0_CF(NCOLOR*(NCOLOR+1)/2)
      INTEGER ML5_0_DENOM
      COMMON /ML5_0_COLOR_MATRIX/ ML5_0_CF,ML5_0_DENOM
      COMPLEX*16 JAMP(NCOLOR), TMP_JAMP(0)
      COMPLEX*16 DUM0,DUM1
      DATA DUM0, DUM1/(0D0, 0D0), (1D0, 0D0)/
C     

C     COLOR DATA
C     

      MATRIX = 0.D0
      CF_INDEX = 0
      DO I = 1, NCOLOR
        ZTEMP = (0.D0,0.D0)
        DO J = I, NCOLOR
          CF_INDEX = CF_INDEX + 1
          ZTEMP = ZTEMP + ML5_0_CF(CF_INDEX)*JAMP(J)
        ENDDO
        MATRIX = MATRIX+ZTEMP*DCONJG(JAMP(I))/ML5_0_DENOM
      ENDDO
      END



      SUBROUTINE ML5_0_GET_INTER(JAMP_1,JAMP_2, INTER)

CF2PY INTENT(OUT) :: INTER
CF2PY INTENT(IN) :: JAMP_1
CF2PY INTENT(IN) :: JAMP_2

      INTEGER I,J
      INTEGER NCOLOR
      PARAMETER (NCOLOR=2)
      INTEGER CF_INDEX
      INTEGER ML5_0_CF(NCOLOR*(NCOLOR+1)/2)
      INTEGER ML5_0_DENOM
      COMMON /ML5_0_COLOR_MATRIX/ ML5_0_CF,ML5_0_DENOM
      COMPLEX*16 JAMP_1(NCOLOR),JAMP_2(NCOLOR),INTER

C     COLOR DATA
C     

      INTER = (0.D0,0.D0)
      CF_INDEX = 0
      DO I = 1, NCOLOR
C       ZTEMP = DCONJG(JAMP_2(I))
        DO J=I, NCOLOR
          CF_INDEX = CF_INDEX +1
          INTER = INTER + ML5_0_CF(CF_INDEX) * (JAMP_1(J) *
     $      DCONJG(JAMP_2(I)) +JAMP_1(I) * DCONJG(JAMP_2(J)))
        ENDDO
      ENDDO
      INTER = INTER/ (2D0*ML5_0_DENOM)

      END



      SUBROUTINE  ML5_0_GET_DENSITY(P, POS, N_CHANGING, ALLOW_HEL,
     $  N_COMB, FLAVOR, ALPHAS, INTER)
C     P momenta
C     NHEL base of helicity that are not changing
C     POS(N_CHNGING): position of the changing helicity
C     n_changing: number of changing helicity
C     ALLOW_HEL(NCOMB, N_CHANGING): combination of helicity to
C      consider (all jamp computed)
C     INTER((NCOMB*NCOMB+1)/2: all interference term (not the
C      symmetric one)
      USE MODEL_OBJECT
      IMPLICIT NONE
CF2PY INTENT(IN) :: P(0:3,4)
CF2PY INTENT(IN) :: POS(N_CHANGING)
CF2PY INTENT(IN) :: N_CHANGING
CF2PY INTENT(IN) :: ALLOW_HEL(N_CHANGING*N_COMB)
CF2PY INTENT(IN) :: N_COMB
CF2PY INTENT(IN) :: FLAVOR(4)
CF2PY INTENT(IN) :: ALPHAS
CF2PY INTENT(OUT) :: INTER(N_COMB*(N_COMB+1)/2)
C     
C     
C     ARGUMENTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=4)
      REAL*8 P(0:3,NEXTERNAL)
      INTEGER THISNHEL(NEXTERNAL)
      INTEGER N_CHANGING, N_COMB
      INTEGER POS(*)
      INTEGER ALLOW_HEL(*)
      INTEGER FLAVOR(NEXTERNAL)
      DOUBLE PRECISION ALPHAS
      DOUBLE COMPLEX INTER(*)
      INTEGER NINTER
      INTEGER NB_NHEL
      DOUBLE COMPLEX, ALLOCATABLE :: TMP_INTER(:)
      PARAMETER (NB_NHEL=16)
C     LOCAL
      INTEGER I,IHEL,IPART
      DOUBLE PRECISION PI
C     
      INTEGER NHEL(NEXTERNAL,NB_NHEL),NTRY
C     put in common block to expose this variable to python interface
      COMMON/ML5_0_PROCESS_NHEL/NHEL
C     
C     include coupling definition to update the value of alphas
C     
      INCLUDE 'coupl.inc'

      NINTER = N_COMB*(N_COMB+1)/2
      ALLOCATE(TMP_INTER(NINTER))
      TMP_INTER(:) = (0D0, 0D0)

      DO I=1, N_COMB*(N_COMB+1)/2
        INTER(I) = 0
      ENDDO

      IF (ALPHAS.NE.0D0) THEN
        PI = 3.141592653589793D0
        G = 2* DSQRT(ALPHAS*PI)
        CALL UPDATE_AS_PARAM()
      ENDIF
      DO IHEL =1, NB_NHEL
        THISNHEL(:) = NHEL(:, IHEL)
        DO IPART=1,N_CHANGING
          IF(THISNHEL(POS(IPART)).NE.ALLOW_HEL(IPART)) GOTO 10  !BYPASS COMPUTATION FOR HELICITY
        ENDDO
        TMP_INTER(:) = 0
        CALL  ML5_0_GET_ALL_INTER(P, THISNHEL, POS, N_CHANGING,
     $    ALLOW_HEL, N_COMB, FLAVOR, TMP_INTER)
        DO I = 1, N_COMB*(N_COMB+1)/2
          INTER(I) = INTER(I) + TMP_INTER(I)
        ENDDO
 10   ENDDO
      RETURN
      DEALLOCATE(TMP_INTER)
      END

      SUBROUTINE  ML5_0_GET_ALL_INTER(P, NHEL, POS, N_CHANGING,
     $  ALLOW_HEL, N_COMB, FLAVOR, INTER)
C     P momenta
C     NHEL base of helicity that are not changing
C     POS(N_CHNGING): position of the changing helicity
C     n_changing: number of changing helicity
C     ALLOW_HEL(NCOMB, N_CHANGING): combination of helicity to
C      consider (all jamp computed)
C     INTER((NCOMB*NCOMB+1)/2: all interference term (not the
C      symmetric one)
      IMPLICIT NONE
CF2PY INTENT(IN) :: P(0:3,4)
CF2PY INTENT(IN) :: NHEL(4)
CF2PY INTENT(IN) :: POS(N_CHANGING)
CF2PY INTENT(IN) :: N_CHANGING
CF2PY INTENT(IN) :: ALLOW_HEL(N_CHANGING*N_COMB)
CF2PY INTENT(IN) :: N_COMB
CF2PY INTENT(IN) :: FLAVOR(4)
CF2PY INTENT(OUT) :: INTER(NCOMB*(NCOMB+1)/2)
C     
C     
C     ARGUMENTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=4)
      REAL*8 P(0:3,NEXTERNAL)
      INTEGER NHEL(NEXTERNAL)
      INTEGER N_CHANGING, N_COMB
      INTEGER POS(*)
      INTEGER ALLOW_HEL(*)
      INTEGER FLAVOR(NEXTERNAL)
      DOUBLE COMPLEX INTER(*)
C     
C     Intermediate array
C     
      INTEGER    NGRAPHS
      PARAMETER (NGRAPHS=1)
      INTEGER    NCOLOR
      PARAMETER (NCOLOR=2)
      INTEGER IC(NEXTERNAL)

      DOUBLE COMPLEX AMP(NGRAPHS)
      DOUBLE COMPLEX, ALLOCATABLE, SAVE :: JAMP(:,:)
      INTEGER, SAVE :: S_NCOMB = 0

C     
C     LOCAL
C     
      INTEGER I,J,SOL,N
      INTEGER FLAV_IDX
      INTEGER ML5_0_GET_FLAVOR_INDEX

      IF (ALLOCATED(JAMP) .AND. S_NCOMB.NE.N_COMB) THEN
        DEALLOCATE(JAMP)
      ENDIF

      IF (.NOT.ALLOCATED(JAMP)) THEN
        S_NCOMB=N_COMB
        ALLOCATE(JAMP(NCOLOR, N_COMB))
      ENDIF
C     ----------
C     BEGIN CODE
C     ----------
      IC(:)=1
      FLAV_IDX = ML5_0_GET_FLAVOR_INDEX(FLAVOR)
C     Unresolved flavor (not an allowed combination): the matrix
C      element and
C     therefore all interference terms are zero.
      IF (FLAV_IDX.EQ.0) THEN
        DO I = 1, N_COMB*(N_COMB+1)/2
          INTER(I) = (0D0, 0D0)
        ENDDO
        RETURN
      ENDIF
      DO I = 1, N_COMB
        DO N = 1, N_CHANGING
          NHEL(POS(N)) = ALLOW_HEL((I-1)*N_CHANGING+N)
        ENDDO
        CALL ML5_0_GET_AMP(P,NHEL,IC,FLAV_IDX,AMP)
        CALL ML5_0_GET_JAMP(AMP,JAMP(1,I))
      ENDDO

      SOL = 0
      DO I = 1, N_COMB
        DO J= I, N_COMB
          SOL = SOL +1
          CALL ML5_0_GET_INTER(JAMP(1,I), JAMP(1,J), INTER(SOL))
        ENDDO
      ENDDO


      RETURN
      END




